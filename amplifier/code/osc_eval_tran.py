#!/usr/bin/env python3
from __future__ import annotations

import math
import re
import subprocess
import tempfile
import time
from pathlib import Path
from typing import Any, Dict, List, Optional, Tuple

from inc_parser import extract_inc_lines

from osc_verifier import verify_inc_osc

OSC_VDD = 1.8
OSC_F_TOL_REL = 0.05
OSC_MIN_VPP = 0.5
OSC_PSTATIC_MAX_MW = 50.0


def _now() -> str:
    return time.strftime('%Y%m%d_%H%M%S')


def build_spice_osc(
    inc_text: str,
    *,
    vdd: float = OSC_VDD,
    rbias_ohm: float = 1000.0,
) -> str:
    """Oscillator deck.

    The model generates passive R/C/L network over nodes {out, vdd, 0} (+ helpers).

    We provide a simple nonlinear negative-conductance core at node out and a bias
    resistor from vdd to out so supply current is measurable.
    """

    inc_lines = [ln.strip() for ln in extract_inc_lines(inc_text) if ln.strip()]
    body: List[str] = []
    for ln in inc_lines:
        toks = ln.split()
        if len(toks) != 5 or toks[0].upper() != 'INC':
            continue
        name, n1, n2, tail = toks[1], toks[2], toks[3], toks[4]
        kind = (name[:1] or '?').upper()
        if kind not in {'R', 'C', 'L'}:
            continue
        body.append(f"{name} {n1} {n2} {tail}")

    # Van-der-Pol-like nonlinear conductance: i = -g1*v + g3*v^3
    core = [
        '.param G1=5e-3 G3=5e-3',
        'BNEG out 0 I = { -G1*V(out) + G3*V(out)^3 }',
        # tiny parasitic cap to avoid singularities
        'CPAR out 0 1p',
    ]

    deck = [
        '* OSC deck generated by VP-SPI (osc)',
        '.options nopage',
        '.option reltol=1e-4 abstol=1e-12 vabstol=1e-9',
        f'VSUP vdd0 0 {float(vdd)}',
        'VMEAS vdd0 vdd 0',
        f'RBIAS_CORE vdd out {float(rbias_ohm)}',
        *core,
        *body,
    ]
    return "\n".join(deck) + "\n"


def _run_ngspice(deck: str, *, timeout_s: float = 60.0) -> str:
    with tempfile.TemporaryDirectory(prefix='osc_spice_') as td:
        td_path = Path(td)
        cir = td_path / 'circuit.cir'
        log = td_path / 'spice.log'
        cir.write_text(deck, encoding='utf-8')
        cmd = ['ngspice', '-b', str(cir), '-o', str(log)]
        try:
            subprocess.run(cmd, check=False, timeout=float(timeout_s), stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL)
        except subprocess.TimeoutExpired:
            return ''
        try:
            return log.read_text(encoding='utf-8', errors='ignore')
        except Exception:
            return ''


_FLOAT_RE = re.compile(r'[+-]?(?:\d+\.?\d*|\d*\.?\d+)(?:[eE][+-]?\d+)?')


def _parse_tran_table(log: str, *, cols: List[str]) -> Tuple[List[float], Dict[str, List[float]]]:
    lines = (log or '').splitlines()
    start = None
    for i, ln in enumerate(lines):
        s = ln.strip().lower()
        if not s.startswith('index'):
            continue
        ok = True
        for c in ['time', *cols]:
            if c.lower() not in s:
                ok = False
                break
        if ok:
            start = i
    if start is None:
        return [], {c: [] for c in cols}

    ts: List[float] = []
    data: Dict[str, List[float]] = {c: [] for c in cols}
    for ln in lines[start + 1:]:
        s = ln.strip()
        if not s or s.startswith('----') or s.lower().startswith('index'):
            continue
        parts = s.split()
        if len(parts) < 2 + len(cols):
            continue
        if not parts[0].isdigit():
            continue
        try:
            t = float(parts[1])
        except Exception:
            continue
        ts.append(t)
        for k, c in enumerate(cols):
            try:
                data[c].append(float(parts[2 + k]))
            except Exception:
                data[c].append(float('nan'))
    return ts, data


def _estimate_freq(ts: List[float], xs: List[float]) -> Optional[float]:
    if not ts or not xs or len(ts) != len(xs) or len(ts) < 20:
        return None

    # Use last half for stability.
    n = len(ts)
    start = n // 2
    t2 = ts[start:]
    x2 = xs[start:]

    mean = sum(x2) / max(1, len(x2))

    crossings: List[float] = []
    for i in range(1, len(x2)):
        a0 = x2[i - 1] - mean
        a1 = x2[i] - mean
        if a0 < 0.0 and a1 >= 0.0:
            # linear interp
            dt = t2[i] - t2[i - 1]
            if dt <= 0:
                continue
            frac = (-a0) / (a1 - a0) if (a1 - a0) != 0 else 0.0
            crossings.append(t2[i - 1] + frac * dt)

    if len(crossings) < 4:
        return None

    periods = [crossings[i] - crossings[i - 1] for i in range(1, len(crossings))]
    periods = [p for p in periods if p > 0]
    if len(periods) < 3:
        return None

    periods.sort()
    med = periods[len(periods) // 2]
    if med <= 0:
        return None
    return float(1.0 / med)


def _parse_meas(log: str, key: str) -> Optional[float]:
    # ngspice prints: "key = <value>"
    for ln in (log or '').splitlines():
        s = ln.strip().lower().replace('\t', ' ')
        if not s.startswith(key.lower()):
            continue
        m = _FLOAT_RE.findall(ln)
        if m:
            try:
                return float(m[-1])
            except Exception:
                return None
    return None


def eval_one_detail_osc(
    inc_text: str,
    *,
    family: str,
    vin: float,
    vout: float,
    sim_timeout_s: float = 60.0,
    min_elems: int = 15,
) -> Dict[str, Any]:
    fam = str(family or '').strip().lower()
    ver = verify_inc_osc(inc_text, family=fam, min_elems=int(min_elems))
    pass_C = bool(ver.ok)
    if not ver.ok:
        return {
            'ok': False,
            'pass_C': pass_C,
            'pass_CV': False,
            'pass_CE': False,
            'violations': ';'.join(ver.violations),
            'canonical_hash': ver.canonical_hash,
            'n_elems': float(ver.n_elems),
            'family': fam,
        }

    f_t = float(vin)
    vpp_t = float(vout)

    # Sim length: ~80 cycles.
    cycles = 80.0
    tstop = max(2e-3, float(cycles) / max(1.0, f_t))
    tstep = min(tstop / 2000.0, 1.0 / (max(1.0, f_t) * 50.0))

    deck = build_spice_osc(inc_text, vdd=float(OSC_VDD))
    deck += f".tran {tstep:.6g} {tstop:.6g} uic\n"
    deck += ".ic V(out)=0.1\n"
    deck += ".print tran v(out) i(VSUP)\n"
    deck += f".meas tran VMAX MAX v(out) FROM {tstop*0.5:.6g} TO {tstop:.6g}\n"
    deck += f".meas tran VMIN MIN v(out) FROM {tstop*0.5:.6g} TO {tstop:.6g}\n"
    deck += f".meas tran IAVG AVG i(VSUP) FROM {tstop*0.5:.6g} TO {tstop:.6g}\n"
    deck += ".end\n"

    log = _run_ngspice(deck, timeout_s=float(sim_timeout_s))

    ts, data = _parse_tran_table(log, cols=['v(out)', 'i(vsup)'])
    vs = data.get('v(out)') or []
    isup = data.get('i(vsup)') or []
    if not ts or not vs:
        return {
            'ok': False,
            'pass_C': pass_C,
            'pass_CV': False,
            'pass_CE': False,
            'canonical_hash': ver.canonical_hash,
            'n_elems': float(ver.n_elems),
            'family': fam,
        }

    v_max = _parse_meas(log, 'vmax')
    v_min = _parse_meas(log, 'vmin')
    if v_max is None:
        v_max = max(vs)
    if v_min is None:
        v_min = min(vs)
    vpp = float(v_max) - float(v_min)

    f_est = _estimate_freq(ts, vs)
    if f_est is None:
        f_est = 0.0

    # Power (rough): use measured IAVG if present.
    iavg = _parse_meas(log, 'iavg')
    if iavg is None:
        if isup:
            iavg = sum(isup[len(isup)//2:]) / max(1, len(isup)//2)
        else:
            iavg = 0.0
    p_mw = abs(float(iavg)) * float(OSC_VDD) * 1e3

    f_err_rel = abs(float(f_est) - float(f_t)) / max(1e-9, float(f_t))
    pass_f = float(f_err_rel) <= float(OSC_F_TOL_REL)
    pass_vpp = float(vpp) >= float(OSC_MIN_VPP)
    pass_p = float(p_mw) <= float(OSC_PSTATIC_MAX_MW)

    pass_cv = bool(pass_f and pass_vpp)
    pass_ce = bool(pass_p)

    return {
        'ok': True,
        'pass_C': pass_C,
        'pass_CV': bool(pass_cv),
        'pass_CE': bool(pass_ce),
        'pass_freq': bool(pass_f),
        'pass_vpp': bool(pass_vpp),
        'pass_p': bool(pass_p),
        'canonical_hash': ver.canonical_hash,
        'n_elems': float(ver.n_elems),
        'family': fam,
        'freq_target_hz': float(f_t),
        'freq_err_rel': float(f_err_rel),
        'freq_hz': float(f_est),
        'vpp_target': float(vpp_t),
        'vpp': float(vpp),
        'pstatic_mw': float(p_mw),
        'pstatic_max_mw': float(OSC_PSTATIC_MAX_MW),
    }
