#!/usr/bin/env python3
from __future__ import annotations

import math
import re
import subprocess
import tempfile
import time
from pathlib import Path
from typing import Any, Dict, List, Optional, Tuple

from dcdc_taskset import Task
from inc_parser import extract_inc_lines

from filter_taskset import (
    FILTER_FAMILY_BPF,
    FILTER_FAMILY_HPF,
    FILTER_FAMILY_LPF,
    FILTER_FAMILY_NOTCH,
)
from filter_verifier import verify_inc_filter

# Global (fixed) filter constraints for this milestone.
FILTER_F_TOL_REL = 0.05
FILTER_MIN_STOP_ATTEN_DB = 20.0
FILTER_MAX_INSERTION_LOSS_DB = 3.0


def _now() -> str:
    return time.strftime('%Y%m%d_%H%M%S')


def task_to_specs(t: Task) -> Dict[str, float]:
    fam = (t.family or '').strip().lower()
    if fam in {FILTER_FAMILY_LPF, FILTER_FAMILY_HPF}:
        return {"fc_hz": float(t.vin), "atten_db": float(t.vout)}
    if fam == FILTER_FAMILY_BPF:
        return {"f0_hz": float(t.vin), "bw_hz": float(t.vout)}
    if fam == FILTER_FAMILY_NOTCH:
        return {"f0_hz": float(t.vin), "atten_db": float(t.vout)}
    return {"f0_hz": float(t.vin), "bw_hz": float(t.vout)}


def _build_spice_filter(inc_text: str, *, rload_ohm: float, fmin: float, fmax: float) -> str:
    inc_lines = [ln.strip() for ln in extract_inc_lines(inc_text) if ln.strip()]

    body: List[str] = []
    for ln in inc_lines:
        toks = ln.split()
        if len(toks) != 5 or toks[0].upper() != 'INC':
            continue
        name, n1, n2, tail = toks[1], toks[2], toks[3], toks[4]
        kind = (name[:1] or '?').upper()
        if kind not in {'R', 'C', 'L'}:
            continue
        body.append(f"{name} {n1} {n2} {tail}")

    deck = [
        '* FILTER deck generated by VP-SPI (filter)',
        '.options nopage',
        '.option reltol=1e-4 abstol=1e-12 vabstol=1e-9',
        'VIN vin 0 AC 1',
        f'RLOAD out 0 {float(rload_ohm)}',
        *body,
        f'.ac dec 40 {float(fmin)} {float(fmax)}',
        '.print ac vm(out)',
        '.end',
    ]
    return "\n".join(deck) + "\n"


def _run_ngspice(deck: str, *, timeout_s: float = 60.0) -> str:
    with tempfile.TemporaryDirectory(prefix='filter_spice_') as td:
        td_path = Path(td)
        cir = td_path / 'circuit.cir'
        log = td_path / 'spice.log'
        cir.write_text(deck, encoding='utf-8')
        cmd = ['ngspice', '-b', str(cir), '-o', str(log)]
        try:
            subprocess.run(cmd, check=False, timeout=float(timeout_s), stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL)
        except subprocess.TimeoutExpired:
            return ''
        try:
            return log.read_text(encoding='utf-8', errors='ignore')
        except Exception:
            return ''


_FLOAT_RE = re.compile(r'[+-]?(?:\d+\.?\d*|\d*\.?\d+)(?:[eE][+-]?\d+)?')


def _parse_ac_table(log: str, *, cols: List[str]) -> Tuple[List[float], Dict[str, List[float]]]:
    lines = (log or '').splitlines()
    start = None
    for i, ln in enumerate(lines):
        s = ln.strip().lower()
        if not s.startswith('index'):
            continue
        ok = True
        for c in ['frequency', *cols]:
            if c.lower() not in s:
                ok = False
                break
        if ok:
            start = i
    if start is None:
        return [], {c: [] for c in cols}

    freqs: List[float] = []
    data: Dict[str, List[float]] = {c: [] for c in cols}
    for ln in lines[start + 1:]:
        s = ln.strip()
        if not s or s.startswith('----') or s.lower().startswith('index'):
            continue
        parts = s.split()
        if len(parts) < 2 + len(cols):
            continue
        if not parts[0].isdigit():
            continue
        try:
            f = float(parts[1])
        except Exception:
            continue
        freqs.append(f)
        for k, c in enumerate(cols):
            try:
                data[c].append(float(parts[2 + k]))
            except Exception:
                data[c].append(float('nan'))
    return freqs, data


def _interp_at(freqs: List[float], mags: List[float], f: float) -> Optional[float]:
    if not freqs or not mags or len(freqs) != len(mags):
        return None
    if f <= freqs[0]:
        return float(mags[0])
    if f >= freqs[-1]:
        return float(mags[-1])
    for i in range(1, len(freqs)):
        if freqs[i] >= f:
            f0, f1 = float(freqs[i-1]), float(freqs[i])
            m0, m1 = float(mags[i-1]), float(mags[i])
            if f1 <= f0:
                return float(m1)
            t = (float(f) - f0) / (f1 - f0)
            return float(m0 + t * (m1 - m0))
    return float(mags[-1])


def _db(x: float) -> float:
    if x <= 0.0:
        return float('-inf')
    return 20.0 * math.log10(float(x))


def eval_one_detail_filter(
    inc_text: str,
    *,
    family: str,
    vin: float,
    vout: float,
    sim_timeout_s: float = 60.0,
    min_elems: int = 20,
    rload_ohm: float = 1000.0,
) -> Dict[str, Any]:
    fam = (family or '').strip().lower()
    ver = verify_inc_filter(inc_text, min_elems=int(min_elems))
    pass_C = bool(ver.ok)
    if not ver.ok:
        return {
            'ok': False,
            'pass_C': pass_C,
            'pass_CV': False,
            'pass_CE': False,
            'violations': ';'.join(ver.violations),
            'canonical_hash': ver.canonical_hash,
            'n_elems': float(ver.n_elems),
            'family': fam,
        }

    spec = task_to_specs(Task(fam, float(vin), float(vout)))

    # Determine sweep range.
    f0 = float(spec.get('fc_hz') or spec.get('f0_hz') or 1e4)
    fmin = max(1.0, float(f0) / 100.0)
    fmax = min(1e8, float(f0) * 100.0)

    deck = _build_spice_filter(inc_text, rload_ohm=float(rload_ohm), fmin=float(fmin), fmax=float(fmax))
    log = _run_ngspice(deck, timeout_s=float(sim_timeout_s))
    freqs, data = _parse_ac_table(log, cols=['vm(out)'])
    mags = data.get('vm(out)') or []
    if not freqs or not mags:
        return {
            'ok': False,
            'pass_C': pass_C,
            'pass_CV': False,
            'pass_CE': False,
            'canonical_hash': ver.canonical_hash,
            'n_elems': float(ver.n_elems),
            'family': fam,
        }

    # Core metrics.
    g0 = float(mags[0])
    gN = float(mags[-1])
    g0_db = _db(g0)
    gN_db = _db(gN)

    pass_cv = False
    pass_ce = False

    # Optional per-metric flags for richer logging/plots.
    pass_fc = False
    pass_stop = False
    pass_f0 = False
    pass_bw = False
    pass_depth = False
    pass_il = False  # insertion loss / passband gain constraint

    fc_t = float("nan")
    f0_t = float("nan")
    bw_t = float("nan")
    atten_db_t = float("nan")

    fc_err_rel = float("nan")
    f0_err_rel = float("nan")
    bw_err_rel = float("nan")
    stop_db = float("nan")

    fc_est = None
    f0_est = None
    bw_est = None
    depth_db = None

    if fam == FILTER_FAMILY_LPF:
        thr = g0 / math.sqrt(2.0) if g0 > 0.0 else 0.0
        fc_est = float(freqs[-1])
        for f, m in zip(freqs, mags):
            if float(m) <= thr and thr > 0.0:
                fc_est = float(f)
                break
        fc_t = float(spec.get('fc_hz') or 0.0)
        atten_db_t = float(spec.get('atten_db') or FILTER_MIN_STOP_ATTEN_DB)
        fc_err_rel = abs(float(fc_est) - float(fc_t)) / max(1e-9, float(fc_t))
        f_stop = min(float(freqs[-1]), float(fc_t) * 10.0)
        m_stop = _interp_at(freqs, mags, f_stop) or 0.0
        stop_db = _db(float(m_stop) / max(1e-12, g0))
        pass_fc = float(fc_err_rel) <= float(FILTER_F_TOL_REL)
        pass_stop = float(stop_db) <= -abs(float(atten_db_t))
        pass_cv = bool(pass_fc and pass_stop)
        pass_il = bool(g0_db >= -float(FILTER_MAX_INSERTION_LOSS_DB))
        pass_ce = bool(pass_il)
    elif fam == FILTER_FAMILY_HPF:
        thr = gN / math.sqrt(2.0) if gN > 0.0 else 0.0
        fc_est = float(freqs[0])
        for f, m in zip(freqs, mags):
            if float(m) >= thr and thr > 0.0:
                fc_est = float(f)
                break
        fc_t = float(spec.get('fc_hz') or 0.0)
        atten_db_t = float(spec.get('atten_db') or FILTER_MIN_STOP_ATTEN_DB)
        fc_err_rel = abs(float(fc_est) - float(fc_t)) / max(1e-9, float(fc_t))
        f_stop = max(float(freqs[0]), float(fc_t) / 10.0)
        m_stop = _interp_at(freqs, mags, f_stop) or 0.0
        stop_db = _db(float(m_stop) / max(1e-12, gN))
        pass_fc = float(fc_err_rel) <= float(FILTER_F_TOL_REL)
        pass_stop = float(stop_db) <= -abs(float(atten_db_t))
        pass_cv = bool(pass_fc and pass_stop)
        pass_il = bool(gN_db >= -float(FILTER_MAX_INSERTION_LOSS_DB))
        pass_ce = bool(pass_il)
    elif fam == FILTER_FAMILY_BPF:
        # Peak and -3dB bandwidth.
        idx_pk = max(range(len(mags)), key=lambda i: float(mags[i]))
        f0_est = float(freqs[idx_pk])
        gpk = float(mags[idx_pk])
        thr = gpk / math.sqrt(2.0) if gpk > 0.0 else 0.0

        # Left/right -3dB points
        f_lo = float(freqs[0])
        for i in range(idx_pk, -1, -1):
            if float(mags[i]) <= thr and thr > 0.0:
                f_lo = float(freqs[i])
                break
        f_hi = float(freqs[-1])
        for i in range(idx_pk, len(mags)):
            if float(mags[i]) <= thr and thr > 0.0:
                f_hi = float(freqs[i])
                break
        bw_est = max(1e-9, float(f_hi - f_lo))

        f0_t = float(spec.get('f0_hz') or 0.0)
        bw_t = float(spec.get('bw_hz') or 1.0)
        f0_err_rel = abs(float(f0_est) - float(f0_t)) / max(1e-9, float(f0_t))
        bw_err_rel = abs(float(bw_est) - float(bw_t)) / max(1e-9, float(bw_t))
        pass_f0 = bool(float(f0_err_rel) <= float(FILTER_F_TOL_REL))
        pass_bw = bool(float(bw_err_rel) <= float(2.0 * FILTER_F_TOL_REL))
        pass_cv = bool(pass_f0 and pass_bw)
        pass_il = bool(_db(gpk) >= -float(FILTER_MAX_INSERTION_LOSS_DB))
        pass_ce = bool(pass_il)
    elif fam == FILTER_FAMILY_NOTCH:
        idx_min = min(range(len(mags)), key=lambda i: float(mags[i]))
        f0_est = float(freqs[idx_min])
        gmin = max(1e-15, float(mags[idx_min]))
        gpass = max(max(1e-15, float(mags[0])), max(1e-15, float(mags[-1])))
        depth_db = _db(float(gmin) / float(gpass))
        f0_t = float(spec.get('f0_hz') or 0.0)
        atten_db_t = float(spec.get('atten_db') or FILTER_MIN_STOP_ATTEN_DB)
        f0_err_rel = abs(float(f0_est) - float(f0_t)) / max(1e-9, float(f0_t))
        pass_f0 = bool(float(f0_err_rel) <= float(FILTER_F_TOL_REL))
        pass_depth = bool(depth_db <= -abs(float(atten_db_t)))
        pass_cv = bool(pass_f0 and pass_depth)
        pass_ce = True
    else:
        pass_cv = False
        pass_ce = False

    return {
        'ok': True,
        'pass_C': pass_C,
        'pass_CV': bool(pass_cv),
        'pass_CE': bool(pass_ce),
        'pass_fc': bool(pass_fc),
        'pass_stop': bool(pass_stop),
        'pass_f0': bool(pass_f0),
        'pass_bw': bool(pass_bw),
        'pass_depth': bool(pass_depth),
        'pass_il': bool(pass_il),
        'canonical_hash': ver.canonical_hash,
        'n_elems': float(ver.n_elems),
        'family': fam,
        'g0_db': float(g0_db),
        'gN_db': float(gN_db),
        'fc_target_hz': float(fc_t),
        'f0_target_hz': float(f0_t),
        'bw_target_hz': float(bw_t),
        'atten_target_db': float(atten_db_t),
        'fc_est': float(fc_est) if fc_est is not None else float('nan'),
        'f0_est': float(f0_est) if f0_est is not None else float('nan'),
        'bw_est': float(bw_est) if bw_est is not None else float('nan'),
        'notch_depth_db': float(depth_db) if depth_db is not None else float('nan'),
        'fc_err_rel': float(fc_err_rel),
        'f0_err_rel': float(f0_err_rel),
        'bw_err_rel': float(bw_err_rel),
        'stop_db': float(stop_db),
    }
