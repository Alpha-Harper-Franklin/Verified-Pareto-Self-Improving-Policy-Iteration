# Global (fixed) amplifier constraints for this first multi-type milestone.
AMP_VDD = 1.8
AMP_PM_MIN_DEG = 60.0
AMP_PSTATIC_MAX_MW = 5.0
AMP_GAIN_TOL_DB = 1.0
AMP_BW_TOL_REL = 0.10
# Family-specific evaluation settings.
AMP_RLOAD_OHM = 10_000.0
AMP_RFPA_RLOAD_OHM = 50.0

AMP_RFPA_VDD = 3.3
AMP_RFPA_PM_MIN_DEG = 45.0
AMP_RFPA_PSTATIC_MAX_MW = 50.0

# Core op-amp model params (family-specific).
# NOTE: amp_rfpa uses a much stronger/low-impedance output stage to make 50? tasks feasible.
AMP_CORE_RDOM_OHM = 1e6
AMP_CORE_CDOM_F = 5e-12
AMP_CORE_RXO_OHM = 2e3
AMP_CORE_COUT_F = 10e-12
AMP_CORE_ROUT_OHM = 200e3
AMP_CORE_GM0 = 2e-3
AMP_CORE_IREF_A = 1e-3

AMP_RFPA_CORE_RDOM_OHM = 1e6
AMP_RFPA_CORE_CDOM_F = 5e-12
AMP_RFPA_CORE_RXO_OHM = 2e3
AMP_RFPA_CORE_COUT_F = 5e-12
AMP_RFPA_CORE_ROUT_OHM = 200e3
AMP_RFPA_CORE_GM0 = 2.0
AMP_RFPA_CORE_IREF_A = 1e-3



def _now() -> str:
    return time.strftime('%Y%m%d_%H%M%S')


def build_spice_amp(
    inc_text: str,
    *,
    vdd: float = AMP_VDD,
    rload_ohm: float = 10_000.0,
    vin_ac: float = 1.0,
    vlg_ac: float = 0.0,
    core: Optional[Dict[str, float]] = None,
) -> str:
    """Build an amplifier SPICE deck.

    The LLM generates only passive R/C/L elements in INC DSL over nodes
    {vin, inv, out, vdd, 0}. We provide the amplifier core as a simple
    2-pole behavioral op-amp.

    Loop-gain injection uses a series AC source between `inv` (feedback node)
    and `inv_int` (op-amp negative input node).
    """

    inc_lines = [ln.strip() for ln in extract_inc_lines(inc_text) if ln.strip()]

    core0 = core or {}
    rdom = float(core0.get('rdom_ohm') or AMP_CORE_RDOM_OHM)
    cdom = float(core0.get('cdom_f') or AMP_CORE_CDOM_F)
    rxo = float(core0.get('rxo_ohm') or AMP_CORE_RXO_OHM)
    cout = float(core0.get('cout_f') or AMP_CORE_COUT_F)
    rout = float(core0.get('rout_ohm') or AMP_CORE_ROUT_OHM)
    gm0 = float(core0.get('gm0') or AMP_CORE_GM0)
    iref = float(core0.get('iref_a') or AMP_CORE_IREF_A)

    # Convert INC -> SPICE primitives.
    body: List[str] = []
    for ln in inc_lines:
        toks = ln.split()
        # normalized is: 'INC <name> <n1> <n2> <tail>'
        if len(toks) != 5 or toks[0].upper() != 'INC':
            continue
        name, n1, n2, tail = toks[1], toks[2], toks[3], toks[4]
        kind = (name[:1] or '?').upper()
        if kind not in {'R', 'C', 'L'}:
            continue
        body.append(f"{name} {n1} {n2} {tail}")

    # Core op-amp model:
    # - Two-pole response: dominant pole at node x, second pole at out
    # - gm scales with sqrt(|I(VSUP)|), so RBIAS trades power vs bandwidth.
    opamp = [
        '* simple 2-pole opamp: non-inv=vin, inv=inv_int, out=out',
        f".param RDOM={rdom:.6g} CDOM={cdom:.6g} RXO={rxo:.6g} COUT={cout:.6g} ROUT={rout:.6g}",
        f".param GM0={gm0:.6g} IREF={iref:.6g}",
        'BGM 0 x I = { (GM0*sqrt(max(1e-12,abs(i(VSUP)))/IREF)) * (V(vin)-V(inv_int)) }',
        'RDOM x 0 {RDOM}',
        'CDOM x 0 {CDOM}',
        'RXO x out {RXO}',
        'COUT out 0 {COUT}',
        'ROUT out 0 {ROUT}',
    ]

    deck = [
        '* AMP deck generated by VP-SPI (amp)',
        f"* vin_ac={float(vin_ac):.3g} vlg_ac={float(vlg_ac):.3g}",
        '.options nopage',
        '.option reltol=1e-4 abstol=1e-12 vabstol=1e-9',
        # Supply with measurable current.
        f"VSUP vdd0 0 {float(vdd)}",
        'VMEAS vdd0 vdd 0',
        # Input source.
        f"VIN vin 0 DC 0 AC {float(vin_ac)}",
        # Loop gain injection (series 0V source, AC set by caller).
        f"VLG inv inv_int DC 0 AC {float(vlg_ac)}",
        # Opamp core uses inv_int.
        *opamp,
        # Generated passive network.
        *body,
        # Fixed load.
        f"RLOAD out 0 {float(rload_ohm)}",
    ]
    return "\n".join(deck) + "\n"


def _run_ngspice(deck: str, *, timeout_s: float = 60.0) -> str:
    with tempfile.TemporaryDirectory(prefix='amp_spice_') as td:
        td_path = Path(td)
        cir = td_path / 'circuit.cir'
        log = td_path / 'spice.log'
        cir.write_text(deck, encoding='utf-8')
        cmd = ['ngspice', '-b', str(cir), '-o', str(log)]
        try:
            subprocess.run(
                cmd,
                check=False,
                timeout=float(timeout_s),
                stdout=subprocess.DEVNULL,
                stderr=subprocess.DEVNULL,
            )
        except subprocess.TimeoutExpired:
            return ''
        try:
            return log.read_text(encoding='utf-8', errors='ignore')
        except Exception:
            return ''


_FLOAT_RE = re.compile(r'[+-]?(?:\d+\.?\d*|\d*\.?\d+)(?:[eE][+-]?\d+)?')


def _parse_branch_current_op(log: str) -> Optional[float]:
    # Look for an OP table header containing "#branch".
    lines = (log or "").splitlines()
    header_idx = None
    for i, ln in enumerate(lines):
        if "#branch" in ln and ln.strip().lower().startswith("index"):
            header_idx = i
    if header_idx is None:
        return None
    # The next line with numeric index contains the branch current.
    for j in range(header_idx + 1, min(header_idx + 15, len(lines))):
        ln = lines[j].strip()
        if not ln:
            continue
        parts = ln.split()
        if len(parts) >= 2 and parts[0].isdigit():
            m = _FLOAT_RE.findall(parts[1])
            if m:
                try:
                    return float(m[0])
                except Exception:
                    return None
    return None


def _parse_ac_table(log: str, *, cols: List[str]) -> Tuple[List[float], Dict[str, List[float]]]:
    # Parse ngspice .print output table.
    # We search for a header line starting with "Index" that contains all requested cols.
    lines = (log or "").splitlines()
    start = None
    for i, ln in enumerate(lines):
        s = ln.strip().lower()
        if not s.startswith("index"):
            continue
        ok = True
        for c in ["frequency", *cols]:
            if c.lower() not in s:
                ok = False
                break
        if ok:
            start = i
    if start is None:
        return [], {c: [] for c in cols}

    freqs: List[float] = []
    data: Dict[str, List[float]] = {c: [] for c in cols}
    for ln in lines[start + 1 :]:
        s = ln.strip()
        if not s:
            continue
        if s.startswith("----") or s.lower().startswith("index"):
            continue
        parts = s.split()
        if len(parts) < 2 + len(cols):
            continue
        if not parts[0].isdigit():
            continue
        try:
            f = float(parts[1])
        except Exception:
            continue
        freqs.append(f)
        for k, c in enumerate(cols):
            try:
                data[c].append(float(parts[2 + k]))
            except Exception:
                data[c].append(float("nan"))
    return freqs, data


def _gain_db_and_bw(freqs: List[float], mags: List[float]) -> Tuple[Optional[float], Optional[float]]:
    if not freqs or not mags or len(freqs) != len(mags):
        return None, None
    g0: Optional[float] = None
    for m in mags:
        try:
            mf = float(m)
        except Exception:
            continue
        if mf > 0.0 and math.isfinite(mf):
            g0 = mf
            break
    if g0 is None:
        return None, None

    g0_db = 20.0 * math.log10(float(g0))
    # -3dB point relative to the low-frequency gain.
    thr_db = float(g0_db) - 20.0 * math.log10(math.sqrt(2.0))

    # Find first crossing from >thr to <=thr and interpolate in (log f, dB) space.
    bw: Optional[float] = None
    prev_f: Optional[float] = None
    prev_db: Optional[float] = None
    for f, m in zip(freqs, mags):
        try:
            ff = float(f)
            mf = float(m)
        except Exception:
            continue
        if not (ff > 0.0) or not (mf > 0.0) or (not math.isfinite(ff)) or (not math.isfinite(mf)):
            continue
        db = 20.0 * math.log10(mf)
        if prev_f is not None and prev_db is not None:
            if prev_db > thr_db >= db:
                try:
                    x1, x2 = math.log10(prev_f), math.log10(ff)
                    y1, y2 = float(prev_db), float(db)
                    if abs(y2 - y1) < 1e-12:
                        bw = ff
                    else:
                        x = x1 + (thr_db - y1) * (x2 - x1) / (y2 - y1)
                        bw = 10.0 ** float(x)
                        if not math.isfinite(float(bw)):
                            bw = ff
                        else:
                            lo, hi = (prev_f, ff) if prev_f <= ff else (ff, prev_f)
                            bw = min(max(float(bw), float(lo)), float(hi))
                except Exception:
                    bw = ff
                break
        prev_f = ff
        prev_db = float(db)

    if bw is None:
        try:
            bw = float(freqs[-1])
        except Exception:
            bw = None
    return float(g0_db), (float(bw) if bw is not None else None)


def _phase_margin(freqs: List[float], v_re: List[float], v_im: List[float]) -> Optional[float]:
    if not freqs or len(freqs) != len(v_re) or len(freqs) != len(v_im):
        return None

    mags: List[float] = []
    phases: List[float] = []
    for re0, im0 in zip(v_re, v_im):
        v = complex(float(re0), float(im0))
        denom = (1.0 - v)
        if abs(denom) < 1e-12:
            t = complex(1e9, 0.0)
        else:
            t = v / denom
        mags.append(abs(t))
        phases.append(math.degrees(math.atan2(t.imag, t.real)))

    # Find first crossing from >1 to <=1.
    cross = None
    for i in range(1, len(freqs)):
        if mags[i - 1] >= 1.0 and mags[i] <= 1.0:
            cross = i
            break
    if cross is None:
        # If the sweep never crosses unity:
        # - max(|T|) < 1  : always below unity -> very stable
        # - min(|T|) > 1  : always above unity (UGF beyond sweep) -> treat as stable for this benchmark
        try:
            if mags:
                if max(mags) < 1.0:
                    return 180.0
                if min(mags) > 1.0:
                    return 180.0
        except Exception:
            pass
        return None

    ph = float(phases[cross])
    pm = 180.0 + ph
    # Map to [0, 180]
    while pm < 0.0:
        pm += 360.0
    while pm > 360.0:
        pm -= 360.0
    if pm > 180.0:
        pm = 360.0 - pm
    return float(pm)


def eval_one_detail_amp(
    inc_text: str,
    *,
    gain_db: float,
    bw_hz: float,
    vdd: float = AMP_VDD,
    pm_min_deg: float = AMP_PM_MIN_DEG,
    pstatic_max_mw: float = AMP_PSTATIC_MAX_MW,
    rload_ohm: float = AMP_RLOAD_OHM,
    core: Optional[Dict[str, float]] = None,
    sim_timeout_s: float = 60.0,
    min_elems: int = 15,
) -> Dict[str, float | bool | str]:
    ver = verify_inc_amp(inc_text, min_elems=int(min_elems))
    pass_C = bool(ver.ok)
    if not ver.ok:
        return {
            'ok': False,
            'pass_C': pass_C,
            'pass_CV': False,
            'pass_CE': False,
            'violations': ';'.join(ver.violations),
            'canonical_hash': ver.canonical_hash,
            'n_elems': float(ver.n_elems),
        }

    # (1) OP: static power from supply current.
    deck_op = build_spice_amp(inc_text, vdd=float(vdd), rload_ohm=float(rload_ohm), vin_ac=0.0, vlg_ac=0.0, core=core)
    deck_op += ".op\n.print op i(VSUP)\n.end\n"
    log_op = _run_ngspice(deck_op, timeout_s=float(sim_timeout_s))
    i_sup = _parse_branch_current_op(log_op)
    if i_sup is None:
        return {
            'ok': False,
            'pass_C': pass_C,
            'pass_CV': False,
            'pass_CE': False,
            'canonical_hash': ver.canonical_hash,
            'n_elems': float(ver.n_elems),
        }
    p_w = abs(float(i_sup)) * float(vdd)
    p_mw = 1e3 * p_w

    # (2) Closed-loop AC (VIN=1, VLG=0).
    deck_ac = build_spice_amp(inc_text, vdd=float(vdd), rload_ohm=float(rload_ohm), vin_ac=1.0, vlg_ac=0.0, core=core)
    deck_ac += ".ac dec 30 1 1e8\n.print ac vm(out)\n.end\n"
    log_ac = _run_ngspice(deck_ac, timeout_s=float(sim_timeout_s))
    freqs, data = _parse_ac_table(log_ac, cols=['vm(out)'])
    mags = data.get('vm(out)') or []
    g_db, bw = _gain_db_and_bw(freqs, mags)
    if g_db is None or bw is None:
        return {
            'ok': False,
            'pass_C': pass_C,
            'pass_CV': False,
            'pass_CE': False,
            'canonical_hash': ver.canonical_hash,
            'n_elems': float(ver.n_elems),
            'pstatic_mw': float(p_mw),
        }

    # (3) Loop gain AC (VIN=0, VLG=1).
    deck_lg = build_spice_amp(inc_text, vdd=float(vdd), rload_ohm=float(rload_ohm), vin_ac=0.0, vlg_ac=1.0, core=core)
    deck_lg += ".ac dec 30 1 1e8\n.print ac vr(inv) vi(inv)\n.end\n"
    log_lg = _run_ngspice(deck_lg, timeout_s=float(sim_timeout_s))
    freqs2, data2 = _parse_ac_table(log_lg, cols=['vr(inv)', 'vi(inv)'])
    pm = _phase_margin(freqs2, data2.get('vr(inv)') or [], data2.get('vi(inv)') or [])

    # Pass/fail (target-matching metrics use "distance to target" instead of a raw range check).
    gain_err_db = abs(float(g_db) - float(gain_db))
    pass_gain = float(gain_err_db) <= float(AMP_GAIN_TOL_DB)

    bw_err_abs = abs(float(bw) - float(bw_hz))
    bw_err_rel = float(bw_err_abs) / max(1e-9, float(bw_hz))
    pass_bw = float(bw_err_rel) <= float(AMP_BW_TOL_REL)

    pass_pm = (pm is not None) and (float(pm) >= float(pm_min_deg))
    pass_p = float(p_mw) <= float(pstatic_max_mw)

    # Map to CV/CE for compatibility with existing logging:
    # - CV: main performance (gain+bw+pm)
    # - CE: power constraint
    pass_cv = bool(pass_gain and pass_bw and pass_pm)
    pass_ce = bool(pass_p)

    return {
        'ok': True,
        'pass_C': pass_C,
        'pass_CV': bool(pass_cv),
        'pass_CE': bool(pass_ce),
        'pass_gain': bool(pass_gain),
        'pass_bw': bool(pass_bw),
        'pass_pm': bool(pass_pm),
        'pass_p': bool(pass_p),
        'canonical_hash': ver.canonical_hash,
        'gain_db_target': float(gain_db),
        'bw_hz_target': float(bw_hz),
        'gain_db': float(g_db),
        'bw_hz': float(bw),
        'gain_err_db': float(gain_err_db),
        'bw_err_abs': float(bw_err_abs),
        'bw_err_rel': float(bw_err_rel),
        'pm_deg': float(pm) if pm is not None else float('nan'),
        'pm_min_deg': float(pm_min_deg),
        'pstatic_mw': float(p_mw),
        'pstatic_max_mw': float(pstatic_max_mw),
        'n_elems': float(ver.n_elems),
    }


def amp_family_params(family: str) -> Dict[str, Any]:
    """Return evaluator params for an amplifier sub-family.

    This keeps a *single* amp evaluator implementation while changing the testbench
    (load, VDD, power budget, PM budget, and core op-amp params) to emulate
    different amplifier classes.
    """

    fam = str(family or '').strip().lower()
    if fam in {'amp', 'amp_op2'}:
        return {
            'vdd': float(AMP_VDD),
            'rload_ohm': float(AMP_RLOAD_OHM),
            'pm_min_deg': float(AMP_PM_MIN_DEG),
            'pstatic_max_mw': float(AMP_PSTATIC_MAX_MW),
            'core': {
                'rdom_ohm': float(AMP_CORE_RDOM_OHM),
                'cdom_f': float(AMP_CORE_CDOM_F),
                'rxo_ohm': float(AMP_CORE_RXO_OHM),
                'cout_f': float(AMP_CORE_COUT_F),
                'rout_ohm': float(AMP_CORE_ROUT_OHM),
                'gm0': float(AMP_CORE_GM0),
                'iref_a': float(AMP_CORE_IREF_A),
            },
        }
    if fam == 'amp_rfpa':
        return {
            'vdd': float(AMP_RFPA_VDD),
            'rload_ohm': float(AMP_RFPA_RLOAD_OHM),
            'pm_min_deg': float(AMP_RFPA_PM_MIN_DEG),
            'pstatic_max_mw': float(AMP_RFPA_PSTATIC_MAX_MW),
            'core': {
                'rdom_ohm': float(AMP_RFPA_CORE_RDOM_OHM),
                'cdom_f': float(AMP_RFPA_CORE_CDOM_F),
                'rxo_ohm': float(AMP_RFPA_CORE_RXO_OHM),
                'cout_f': float(AMP_RFPA_CORE_COUT_F),
                'rout_ohm': float(AMP_RFPA_CORE_ROUT_OHM),
                'gm0': float(AMP_RFPA_CORE_GM0),
                'iref_a': float(AMP_RFPA_CORE_IREF_A),
            },
        }
    # Default alias
    return {
        'vdd': float(AMP_VDD),
        'rload_ohm': float(AMP_RLOAD_OHM),
        'pm_min_deg': float(AMP_PM_MIN_DEG),
        'pstatic_max_mw': float(AMP_PSTATIC_MAX_MW),
        'core': {
            'rdom_ohm': float(AMP_CORE_RDOM_OHM),
            'cdom_f': float(AMP_CORE_CDOM_F),
            'rxo_ohm': float(AMP_CORE_RXO_OHM),
            'cout_f': float(AMP_CORE_COUT_F),
            'rout_ohm': float(AMP_CORE_ROUT_OHM),
            'gm0': float(AMP_CORE_GM0),
            'iref_a': float(AMP_CORE_IREF_A),
        },
    }


def eval_one_detail_amp_family(
    inc_text: str,
    *,
    family: str,
    gain_db: float,
    bw_hz: float,
    sim_timeout_s: float = 60.0,
    min_elems: int = 15,
) -> Dict[str, float | bool | str]:
    params = amp_family_params(str(family))
    core = params.get('core')
    if not isinstance(core, dict):
        core = None
    out = eval_one_detail_amp(
        inc_text,
        gain_db=float(gain_db),
        bw_hz=float(bw_hz),
        vdd=float(params['vdd']),
        pm_min_deg=float(params['pm_min_deg']),
        pstatic_max_mw=float(params['pstatic_max_mw']),
        rload_ohm=float(params['rload_ohm']),
        core=core,
        sim_timeout_s=float(sim_timeout_s),
        min_elems=int(min_elems),
    )
    # annotate for debugging
    try:
        out['family'] = str(family)
    except Exception:
        pass
    return out





def task_to_specs(t: Task) -> Tuple[float, float]:
    """Interpret Task('amp', vin, vout) as (gain_db, bw_hz)."""
    return float(t.vin), float(t.vout)
